    @EventListener(ApplicationReadyEvent.class)
    public void seed() {
        if (!seedEnabled) return;

        boolean usersNeeded   = !userRepository.existsByEmployeeId("EMP001");
        boolean nomikaiNeeded = postRepository.findByCategoryOrderByCreatedAtDesc("お誘い").isEmpty();
        boolean eventsNeeded  = postRepository.findByCategoryOrderByCreatedAtDesc("その他のイベント").isEmpty();
        boolean hobbyNeeded   = postRepository.findByCategoryOrderByCreatedAtDesc("趣味・おしゃべり").isEmpty();
        boolean newsNeeded    = postRepository.findByCategoryOrderByCreatedAtDesc("社内ニュース・イベント").isEmpty();
        boolean itNeeded      = postRepository.findByCategoryOrderByCreatedAtDesc("IT関連ニュース").isEmpty();
        boolean commentsNeeded = commentRepository.count() < 10;

        if (!usersNeeded && !nomikaiNeeded && !eventsNeeded && !hobbyNeeded && !newsNeeded && !itNeeded) {
            log.info("DataSeeder: 全データ投入済みのためスキップします");
            return;
        }

        log.info("DataSeeder: テストデータの投入を開始します...");

        List<User> users;
        if (usersNeeded) {
            users = seedUsers();
            log.info("DataSeeder: ユーザー{}名を投入しました", users.size());
        } else {
            users = userRepository.findAll().stream()
                    .filter(u -> u.getEmployeeId() != null && u.getEmployeeId().startsWith("EMP"))
                    .sorted(Comparator.comparing(User::getEmployeeId))
                    .collect(Collectors.toList());
            log.info("DataSeeder: 既存ユーザー{}名を使用", users.size());
        }

        List<Post> seededPosts = seedPosts(users);
        log.info("DataSeeder: 投稿{}件を投入しました", seededPosts.size());

        if (commentsNeeded) {
            List<Post> forComments = new ArrayList<>();
            forComments.addAll(postRepository.findByCategoryOrderByCreatedAtDesc("お誘い"));
            forComments.addAll(postRepository.findByCategoryOrderByCreatedAtDesc("その他のイベント"));
            forComments.addAll(postRepository.findByCategoryOrderByCreatedAtDesc("趣味・おしゃべり"));
            if (!forComments.isEmpty()) seedComments(users, forComments);
        }

        log.info("DataSeeder: 完了");
    }

