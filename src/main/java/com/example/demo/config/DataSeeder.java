    private Post p(String title, String content, String category, User author,
                   String eventDate, String deadline, String location,
                   Integer capacity, int viewCount, int daysAgo) {
        Post post = new Post();
        post.setTitle(title);
        post.setContent(content);
        post.setCategory(category);
        post.setAuthor(author);
        if (eventDate != null) post.setEventDate(LocalDate.parse(eventDate));
        if (deadline  != null) post.setDeadline(LocalDate.parse(deadline));
        if (location  != null) post.setLocation(location);
        if (capacity  != null) post.setCapacity(capacity);
        post.setViewCount(viewCount + (int)(Math.random() * 20));
        Post saved = postRepository.save(post);
        postRepository.updateCreatedAt(saved.getId(), LocalDateTime.now().minusDays(daysAgo));
        return saved;
    }
